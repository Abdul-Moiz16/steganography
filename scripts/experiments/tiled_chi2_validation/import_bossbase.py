"""Import BOSSBase 1.01 into our pipeline's run layout for tile-local chi^2 validation.

BOSSBase 1.01 is the field-reference steganalysis dataset: 10,000 grayscale
512x512 images sourced from 7 camera models, distributed as PGM files.  It is
the dataset used by Holub & Fridrich (DCTR), Boroumand et al. (SRNet), and
most other modern steganalysis evaluations.  Running our tile-local chi^2
experiments on BOSSBase gives the validation external academic comparability
beyond our caption-matched real-vs-AI corpus.

This script:

1. Reads BOSSBase covers from ``--bossbase-dir`` (one PGM per image).
2. JPEG-encodes each cover at the requested quality ``--quality`` and writes
   it under ``<out_run>/covers/real/g<NNNN>__src-real.jpg``.
3. For each (payload_level, encryption) cell, embeds a JSteg-style DCT-LSB
   stego via the pipeline's existing :func:`embed_dct_lsb_jpeg`, then writes
   it under ``<out_run>/stego/dct/<payload>/<encryption>/real/g<NNNN>__src-real__m-dct__p-<payload>__e-<encryption>.jpg``.
4. Writes ``manifests/covers_real.csv`` (one row per cover, with SHA256s
   of both the input PGM and the output cover JPEG) and
   ``manifests/stegos.csv`` (one row per stego, with SHA256s of the stego
   bytes and a back-pointer to the cover SHA256), so the import is fully
   auditable and the experiment scripts (exp1, exp3, exp4) can iterate
   the new run directory by passing ``--payload-levels`` and ``--sources``.

The payload axis here is the six-level bpnzAC sweep
``{0.05, 0.10, 0.20, 0.30, 0.40, 0.50}`` -- intentionally different from
the main pipeline's three coarse low/medium/high points so the BOSSBase
validation reports a full operating curve.  Directory names are
``p005..p050`` (zero-padded so string sort = numeric order).

Importantly, this importer does NOT call :class:`PipelineRunner` --
it bypasses the runner's strict n_groups check and skip-if-exists
plumbing because BOSSBase covers are sourced from disk rather than
generated by our HF/Flickr download pipeline.  The output directory
is nonetheless format-compatible with the runner so that downstream
analysis (computer_metrics_from_predictions, etc.) still works if you
later choose to invoke it.

Downloading BOSSBase
--------------------
BOSSBase 1.01 is distributed by the Czech Technical University.  The
canonical mirror has moved a few times over the years; as of writing
the following options are known-good:

  - http://agents.fel.cvut.cz/boss/  (CVUT, official)
  - http://dde.binghamton.edu/download/  (Binghamton mirror page;
    follow the "BOSSbase" link)

Download the grayscale-PGM version (~3 GB extracted, 10,000 files
named ``1.pgm``, ``2.pgm``, ..., ``10000.pgm``, each 512x512 8-bit).

Usage
-----
    # Full BOSSBase at Q=95 (matches our caption-matched main run):
    venv312/bin/python -m scripts.experiments.tiled_chi2_validation.import_bossbase \\
        --bossbase-dir /path/to/BOSSbase_1.01 \\
        --out-run runs/bossbase_q95 \\
        --quality 95

    # Same at Q=75 (matches DCTR's headline JPEG quality):
    ... --quality 75 --out-run runs/bossbase_q75

    # Quick smoke run on first 100 images only (~3 min instead of ~3h):
    ... --n-images 100 --out-run runs/bossbase_q95_smoke

After import, run any validation experiment with --run pointed at the new
dir, plus --payload-levels and --sources flags that match BOSSBase's six
numerical payload levels and single 'real' source:

    venv312/bin/python -m scripts.experiments.tiled_chi2_validation.exp1_tsweep \\
        --run runs/bossbase_q95 \\
        --payload-levels p005 p010 p020 p030 p040 p050 \\
        --sources real
"""
from __future__ import annotations

import argparse
import csv
import hashlib
import io
import multiprocessing as mp
import os
import sys
import time
from pathlib import Path

from PIL import Image

# Project-root path setup so ``from src...`` imports resolve.
HERE = Path(__file__).resolve().parent
ROOT = HERE.parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from src.embedding.dct import dct_payload_capacity_bytes, embed_dct_lsb_jpeg  # noqa: E402
from src.embedding.outguess import (  # noqa: E402
    embed_outguess_jpeg,
    outguess_payload_capacity_bytes,
)


def _capacity_for_method(method: str, cover_bytes: bytes, fill_rate: float) -> int:
    if method == "jsteg":
        return dct_payload_capacity_bytes(cover_bytes, fill_rate)
    if method == "outguess":
        return outguess_payload_capacity_bytes(cover_bytes, fill_rate)
    raise ValueError(f"unknown method: {method!r}")


def _embed_for_method(method: str, cover_bytes: bytes, payload_bytes: bytes,
                     fill_rate: float, jpeg_quality: int, seed: int) -> bytes:
    if method == "jsteg":
        return embed_dct_lsb_jpeg(cover_bytes, payload_bytes, fill_rate,
                                  jpeg_quality=jpeg_quality)
    if method == "outguess":
        # OutGuess uses the seed for the embedding-path shuffle.  Mix in the
        # group_id-derived caller seed for per-cover determinism but keep the
        # global flow deterministic per (seed, group_id, payload_level,
        # encryption) -- which is exactly what _make_payload already does for
        # the payload bytes, so passing the same composite seed here keeps
        # everything deterministic from one upstream seed.
        return embed_outguess_jpeg(cover_bytes, payload_bytes, fill_rate,
                                   jpeg_quality=jpeg_quality, seed=seed)
    raise ValueError(f"unknown method: {method!r}")


# Six-level bpnzAC sweep matching the validation methodology decided for
# BOSSBase: 0.05, 0.10, 0.20, 0.30, 0.40, 0.50 bits per non-zero AC
# coefficient.  Names are zero-padded so directory listings and CSV
# string-sorts agree with numeric order (p005 < p010 < ... < p050).
#
# This is INTENTIONALLY different from the main pipeline's three-level
# low/medium/high naming -- the main pipeline reports three coarse points
# on its caption-matched corpus, while BOSSBase validation reports the
# full operating curve.  The experiment runners (exp1/exp3/exp4) accept
# --payload-levels p005 p010 p020 p030 p040 p050 to iterate this axis.
PAYLOAD_FILL_RATES: list[tuple[str, float]] = [
    ("p005", 0.05),
    ("p010", 0.10),
    ("p020", 0.20),
    ("p030", 0.30),
    ("p040", 0.40),
    ("p050", 0.50),
]
ENCRYPTIONS = ("plain", "encrypted")
IMAGE_SIZE = (512, 512)  # BOSSBase native; standardise just in case.


def _sha256_hex(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


# ---------------------------------------------------------------------------
# Multiprocessing workers (top-level so spawn-mode workers can pickle them).
# Both workers do all their own I/O so the main process only collects the
# returned metadata records and writes the manifests at the end.
# ---------------------------------------------------------------------------

def _encode_one_cover(task: tuple) -> dict:
    """Stage 1 worker: read PGM, JPEG-encode, write, return manifest row."""
    import datetime  # noqa: F401 -- numpy/multiprocessing PyCapsule_Import workaround
    pgm_path, group_id, jpeg_path, quality, out_run_parent = task
    pgm_path = Path(pgm_path); jpeg_path = Path(jpeg_path); out_run_parent = Path(out_run_parent)
    pgm_bytes = pgm_path.read_bytes()
    pgm_sha256 = _sha256_hex(pgm_bytes)
    if not jpeg_path.exists():
        _pgm_to_jpeg(pgm_bytes, jpeg_path, quality=quality)
    cover_bytes = jpeg_path.read_bytes()
    cover_sha256 = _sha256_hex(cover_bytes)
    return {
        "group_id": group_id,
        "source": "real",
        "dataset": "BOSSBase_1.01",
        "orig_id": pgm_path.stem,
        "caption_id": f"bossbase-{pgm_path.stem}",
        "caption_text": "",
        "spatial_path": "",
        "frequency_path": str(jpeg_path.relative_to(out_run_parent)),
        "qc_pass": "true",
        "qc_score": "1.0",
        "seed": "0",
        "pgm_sha256": pgm_sha256,
        "cover_sha256": cover_sha256,
    }


def _embed_one_stego(task: tuple) -> dict | None:
    """Stage 2 worker: load cover, build payload, embed via the dispatched
    method, write stego, return manifest row.

    The method dispatch is per-task (rather than per-pool) so future runs
    that want to interleave methods in one importer call can do so.

    Returns None for pathological covers with insufficient AC capacity (skipped
    to avoid raising in the embedder; consistent with the original serial loop).

    The output directory layout uses ``stego/dct/...`` regardless of method
    because (a) all χ²-family detectors and the validation scaffold expect
    the ``stego/<method-axis>/<payload>/<encryption>/<source>/`` shape, and
    (b) we run separate import_bossbase invocations per method (and write
    them to separate top-level run directories like ``runs/bossbase_q95_jsteg/``
    vs ``runs/bossbase_q95_outguess/``), so there is no name collision.  The
    inner ``stego/dct/`` axis is kept as the JPEG-frequency-branch label,
    matching the rest of the pipeline; the actual embedding method is
    recorded in the ``method`` column of the per-stego manifest.
    """
    import datetime  # noqa: F401 -- numpy/multiprocessing PyCapsule_Import workaround
    (cover_record, payload_level, fill_rate, encryption,
     out_run, out_run_parent, payload_seed, jpeg_quality, embedding_method) = task
    out_run = Path(out_run); out_run_parent = Path(out_run_parent)
    gid = cover_record["group_id"]
    cover_jpeg = (out_run_parent / cover_record["frequency_path"]).read_bytes()
    capacity = _capacity_for_method(embedding_method, cover_jpeg, fill_rate)
    if capacity <= 0:
        return None
    payload_bytes = _make_payload(
        seed=payload_seed, group_id=gid,
        payload_level=payload_level, encryption=encryption,
        n_bytes=capacity,
    )
    # Per-(gid, payload, encryption) deterministic shuffle seed for OutGuess.
    # Reusing the payload seed string as a hash makes the choice deterministic
    # and reproducible from (payload_seed, group_id, payload_level, encryption).
    import hashlib as _h
    method_seed = int(_h.sha256(
        f"{payload_seed}|{gid}|{payload_level}|{encryption}|{embedding_method}".encode()
    ).hexdigest()[:8], 16)
    stego_jpeg = _embed_for_method(
        embedding_method, cover_jpeg, payload_bytes, fill_rate,
        jpeg_quality=jpeg_quality, seed=method_seed,
    )
    stego_dir = out_run / "stego" / "dct" / payload_level / encryption / "real"
    stego_dir.mkdir(parents=True, exist_ok=True)
    stego_path = stego_dir / f"g{gid:04d}__src-real__m-dct__p-{payload_level}__e-{encryption}.jpg"
    stego_path.write_bytes(stego_jpeg)
    return {
        "group_id": gid,
        "source": "real",
        "method": "dct",                      # JPEG-frequency branch axis
        "embedding": embedding_method,        # jsteg | outguess
        "payload_level": payload_level,
        "fill_rate": fill_rate,
        "encryption": encryption,
        "stego_path": str(stego_path.relative_to(out_run_parent)),
        "stego_sha256": _sha256_hex(stego_jpeg),
        "cover_sha256": cover_record["cover_sha256"],
        "payload_bytes": capacity,
    }


def main() -> None:
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--bossbase-dir", type=Path, required=True,
                   help="Directory containing BOSSBase PGM files (1.pgm ... 10000.pgm).")
    p.add_argument("--out-run", type=Path, required=True,
                   help="Output run directory, e.g. runs/bossbase_q95.")
    p.add_argument("--quality", type=int, default=95,
                   help="JPEG quality factor for cover encoding (default 95; DCTR uses 75).")
    p.add_argument("--n-images", type=int, default=None,
                   help="Cap the number of images to process (default: all PGMs found, "
                        "up to 10000). Use a small value for a quick smoke run.")
    p.add_argument("--payload-seed", type=int, default=42,
                   help="Seed for the per-cover deterministic payload PRNG (default 42).")
    p.add_argument("--skip-stego", action="store_true",
                   help="Only emit covers + manifest, skip stego embedding "
                        "(useful to inspect the cover layout before committing "
                        "to the slow embedding step).")
    p.add_argument("--n-workers", type=int,
                   default=max(1, (os.cpu_count() or 2) // 2),
                   help="Worker processes for parallel encode+embed (default: cpu_count/2). "
                        "On a 16-core box, --n-workers 16 takes the full import from ~3h to ~10-15 min.")
    p.add_argument("--method", choices=["jsteg", "outguess"], default="jsteg",
                   help="Embedding algorithm to use. "
                        "'jsteg' (default) = Westfeld 1999 sequential LSB-replacement on "
                        "non-zero AC DCT coefficients, the embedding the chi^2 family "
                        "was designed to detect. "
                        "'outguess' = Provos 2001 randomised-path LSB-replacement with "
                        "histogram-preserving correction, designed to defeat the chi^2 "
                        "test on the global histogram.")
    args = p.parse_args()

    pgm_paths = _discover_pgms(args.bossbase_dir, args.n_images)
    print(f"Found {len(pgm_paths)} BOSSBase PGM(s) under {args.bossbase_dir}")

    covers_dir = args.out_run / "covers" / "real"
    covers_dir.mkdir(parents=True, exist_ok=True)
    manifests_dir = args.out_run / "manifests"
    manifests_dir.mkdir(parents=True, exist_ok=True)

    # --- Stage 1: cover encoding (parallel) -----------------------------
    print(f"\n--- Stage 1: encode covers at Q={args.quality} with {args.n_workers} workers ---")
    encode_tasks = [
        (str(pgm), idx,
         str(covers_dir / f"g{idx:04d}__src-real.jpg"),
         args.quality, str(args.out_run.parent))
        for idx, pgm in enumerate(pgm_paths, start=1)
    ]
    cover_records: list[dict] = []
    t0 = time.time()
    if args.n_workers <= 1:
        for i, task in enumerate(encode_tasks, start=1):
            cover_records.append(_encode_one_cover(task))
            if i % 1000 == 0:
                print(f"  {i}/{len(encode_tasks)} covers encoded ({(time.time() - t0) / 60:.1f} min)")
    else:
        ctx = mp.get_context("spawn")
        with ctx.Pool(args.n_workers) as pool:
            for i, rec in enumerate(
                pool.imap_unordered(_encode_one_cover, encode_tasks, chunksize=8),
                start=1,
            ):
                cover_records.append(rec)
                if i % 1000 == 0:
                    print(f"  {i}/{len(encode_tasks)} covers encoded ({(time.time() - t0) / 60:.1f} min)")
    # Sort by group_id so the manifest is in the expected ascending order
    # despite imap_unordered's arbitrary return order.
    cover_records.sort(key=lambda r: r["group_id"])
    print(f"  done: {len(cover_records)} covers in {(time.time() - t0) / 60:.1f} min")

    # --- Write covers_real.csv (so exp1..5 can iterate by group_id) -----
    manifest_path = manifests_dir / "covers_real.csv"
    with manifest_path.open("w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=list(cover_records[0].keys()))
        w.writeheader()
        w.writerows(cover_records)
    print(f"  wrote {manifest_path} ({len(cover_records)} rows)")

    if args.skip_stego:
        print("\n--skip-stego given; covers + manifest written, exiting.")
        return

    # --- Stage 2: stego embedding (parallel, the slow part) -------------
    print(f"\n--- Stage 2: embed {args.method!r} stegos at {len(PAYLOAD_FILL_RATES)} payloads "
          f"x {len(ENCRYPTIONS)} encryptions with {args.n_workers} workers ---")
    total = len(cover_records) * len(PAYLOAD_FILL_RATES) * len(ENCRYPTIONS)
    embed_tasks = []
    for payload_level, fill_rate in PAYLOAD_FILL_RATES:
        for encryption in ENCRYPTIONS:
            for record in cover_records:
                embed_tasks.append((
                    record, payload_level, fill_rate, encryption,
                    str(args.out_run), str(args.out_run.parent),
                    args.payload_seed, args.quality, args.method,
                ))
    print(f"  built {len(embed_tasks)} embed tasks")

    stego_records: list[dict] = []
    done = 0; skipped = 0
    t0 = time.time()

    def _accumulate(rec):
        nonlocal done, skipped
        if rec is None:
            skipped += 1
        else:
            stego_records.append(rec)
        done += 1
        if done % 500 == 0:
            rate = done / max(time.time() - t0, 1e-3)
            eta_min = (total - done) / max(rate, 1e-3) / 60
            print(f"  {done}/{total} embed tasks complete ({rate:.0f} img/s, "
                  f"ETA {eta_min:.1f} min, skipped={skipped})")

    if args.n_workers <= 1:
        for task in embed_tasks:
            _accumulate(_embed_one_stego(task))
    else:
        ctx = mp.get_context("spawn")
        with ctx.Pool(args.n_workers) as pool:
            for rec in pool.imap_unordered(_embed_one_stego, embed_tasks, chunksize=8):
                _accumulate(rec)
    # Sort for deterministic manifest order matching what the serial
    # implementation produced (outer payload, encryption; inner gid).
    payload_order = {name: i for i, (name, _) in enumerate(PAYLOAD_FILL_RATES)}
    encryption_order = {enc: i for i, enc in enumerate(ENCRYPTIONS)}
    stego_records.sort(key=lambda r: (
        payload_order.get(r["payload_level"], 99),
        encryption_order.get(r["encryption"], 99),
        r["group_id"],
    ))
    print(f"  done: {len(stego_records)} stegos in {(time.time() - t0) / 60:.1f} min "
          f"({skipped} covers skipped for insufficient AC capacity)")

    # --- Write stegos manifest -----------------------------------------
    stego_manifest = manifests_dir / "stegos.csv"
    if stego_records:
        with stego_manifest.open("w", newline="") as f:
            w = csv.DictWriter(f, fieldnames=list(stego_records[0].keys()))
            w.writeheader()
            w.writerows(stego_records)
        print(f"  wrote {stego_manifest} ({len(stego_records)} rows)")

    payload_arg = " ".join(name for name, _ in PAYLOAD_FILL_RATES)
    print(f"\nBOSSBase imported to {args.out_run}")
    print("Next step: validate by running any experiment.  BOSSBase uses the "
          "six-level bpnzAC axis and single 'real' source, so pass:")
    print(f"    venv312/bin/python -m scripts.experiments.tiled_chi2_validation.exp1_tsweep \\")
    print(f"        --run {args.out_run} \\")
    print(f"        --payload-levels {payload_arg} \\")
    print(f"        --sources real")


def _discover_pgms(root: Path, n_images: int | None) -> list[Path]:
    """Find BOSSBase PGM files under ``root``, sorted by numeric stem.

    Supports both flat layouts (``root/1.pgm`` ...) and one level of
    nesting (``root/something/1.pgm``).
    """
    if not root.exists():
        raise SystemExit(f"--bossbase-dir not found: {root}")
    pgms = sorted(root.rglob("*.pgm"), key=lambda p: int(p.stem) if p.stem.isdigit() else p.stem)
    if not pgms:
        raise SystemExit(f"No .pgm files under {root}. Check the BOSSBase download.")
    if n_images is not None:
        pgms = pgms[:n_images]
    return pgms


def _pgm_to_jpeg(pgm_bytes: bytes, out_path: Path, *, quality: int) -> None:
    """Decode PGM bytes (grayscale 8-bit), JPEG-encode at quality, save to out_path."""
    with Image.open(io.BytesIO(pgm_bytes)) as im:
        im = im.convert("L")  # PGM is already grayscale but force just in case
        if im.size != IMAGE_SIZE:
            # BOSSBase is 512x512 by construction; this is defence-in-depth.
            im = im.resize(IMAGE_SIZE, Image.LANCZOS)
        buf = io.BytesIO()
        im.save(buf, format="JPEG", quality=quality, subsampling=0, optimize=False)
        out_path.write_bytes(buf.getvalue())


def _make_payload(seed: int, group_id: int, payload_level: str,
                  encryption: str, n_bytes: int) -> bytes:
    """Deterministic per-cover payload bytestream.

    Matches the main pipeline's convention: a seeded RNG keyed on
    (seed, group_id, payload_level, encryption) so the same (cover,
    cell) always gets the same payload, but different cells get
    different payloads.  For BOSSBase imports we use plain random
    bytes regardless of encryption label (the chi^2 detector is
    encryption-invariant by construction; the encryption label is
    preserved as a stratification axis only).
    """
    import random
    # Python 3.12 no longer accepts tuple seeds for random.Random; serialise the
    # composite key to a deterministic string and let the RNG hash it itself.
    rng = random.Random(f"{seed}|{group_id}|{payload_level}|{encryption}")
    return rng.randbytes(n_bytes)


if __name__ == "__main__":
    main()
